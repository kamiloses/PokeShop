export class Pokemon {
    id;
    name;
    description;
    type;
    rarity;
    price;

    constructor(id, name, description, type, rarity, price) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.type = type;
        this.rarity = rarity;
        this.price = price;


    }

}